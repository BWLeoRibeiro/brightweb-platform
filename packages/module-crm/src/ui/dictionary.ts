export const defaultCrmStages: CrmStageConfig[] = [
  { value: "lead", label: "Novo", token: "--crm-stage-lead" },
  { value: "qualified", label: "Qualificado", token: "--crm-stage-qualified" },
  { value: "proposal", label: "Proposta", token: "--crm-stage-proposal" },
  { value: "won", label: "Ganho", token: "--crm-stage-won" },
  { value: "lost", label: "Perdido", token: "--crm-stage-lost" },
];

export function resolveCrmStages(dictionary: CrmUiDictionary, stages?: CrmStageConfig[]) {
  return stages ?? defaultCrmStages.map((stage) => ({ ...stage, label: dictionary.stages[stage.value] }));
}

export const defaultCrmStages: CrmStageConfig[] = [
  { value: "lead", label: "Lead", token: "--crm-stage-lead" },
  { value: "qualified", label: "Qualified", token: "--crm-stage-qualified" },
  { value: "proposal", label: "Proposal", token: "--crm-stage-proposal" },
  { value: "won", label: "Won", token: "--crm-stage-won" },
  { value: "lost", label: "Lost", token: "--crm-stage-lost" },
];

export function resolveCrmStages(dictionary: CrmUiDictionary, stages?: CrmStageConfig[]) {
  return stages ?? defaultCrmStages.map((stage) => ({ ...stage, label: dictionary.stages[stage.value] }));
}
